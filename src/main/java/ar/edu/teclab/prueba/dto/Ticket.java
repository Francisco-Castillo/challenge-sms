package ar.edu.teclab.prueba.dto;

/**
 *
 * @author fcastillo
 */
public class Ticket {

    private Integer id;
    private String comment;

    public Ticket() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

}
